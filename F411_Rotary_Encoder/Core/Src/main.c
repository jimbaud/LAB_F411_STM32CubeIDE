void ENC_vRotEnc_Handler(ENC_tenDir ENC__enDir)
{
	if(ENC__enDir == 0)
	{
		count++;
	}
	if(ENC__enDir > 0)
	{
		count--;
	}
	sprintf(rotary_value, "%i", count);
	flag_RB = 1;
}

void set_cursor(int val)
{
	if(val < 0)
	{
		LCD_put_cur(0, 0, LCD_ADDR);
		LCD_SendString(LCD_ADDR, ">");
		count = 0;
		screen = 1;
	}
	if(val > 5)
	{
		LCD_put_cur(1, 0, LCD_ADDR);
		LCD_SendString(LCD_ADDR, ">");
		count = 5;
		screen = 3;
	}
	else {
		if (val == 0 || val == 1)
		{
			screen = 1;
		}
		if (val == 2 || val == 3)
		{
			screen = 2;
		}
		if (val == 4 || val == 5)
		{
			screen = 3;
		}
		val = val%2;
		LCD_put_cur(val, 0, LCD_ADDR);
		LCD_SendString(LCD_ADDR, ">");
	}

}

void set_screen (int screen)
{
	if (screen == 1)
	{
		LCD_put_cur(0, 1, LCD_ADDR);
		LCD_SendString(LCD_ADDR, "Programme 1");
		LCD_put_cur(1, 1, LCD_ADDR);
		LCD_SendString(LCD_ADDR, "Programme 2");
	}
	if (screen == 2)
	{
		LCD_put_cur(0, 1, LCD_ADDR);
		LCD_SendString(LCD_ADDR, "Programme 3");
		LCD_put_cur(1, 1, LCD_ADDR);
		LCD_SendString(LCD_ADDR, "Programme 4");
	}
	if (screen == 3)
	{
		LCD_put_cur(0, 1, LCD_ADDR);
		LCD_SendString(LCD_ADDR, "Programme 5");
		LCD_put_cur(1, 1, LCD_ADDR);
		LCD_SendString(LCD_ADDR, "Programme 6");
	}
}

